function drawCell(id) {
  if (id < 0) return;
  var ix = id & 7;
  var iy = id >> 3;
  var x = .1*(ix + 1);
  var y = .1*(8 - iy);
  var p0 = [x+.1, y+.1];
  var p1 = [x+.1, y];
  var p2 = [x, y];
  var p3 = [x, y+.1];
  pen.lines(p0,p1,p2,p3,p0);
}

function drawBoard(dark='#B88762',light='#EED7B0') {
  ctx.clearRect(0,0,canvas.width,canvas.height);
  var s = canvas.width;
  ctx.fillStyle = dark;
  ctx.fillRect(.1*s,.1*s,.8*s,.8*s);
  ctx.beginPath();
  ctx.fillStyle = light;
  for (var i = 0; i < 64; i++) {
    var x = i & 7;
    var y = i >> 3;
    var p = (x + y) % 2;
    if (!p) continue;
    drawCell(i);
  }
  ctx.fill();
  ctx.closePath();
}

function drawGridlines(color='black',lineWidth=2) {
  ctx.strokeStyle = color;
  ctx.lineWidth = lineWidth;
  ctx.beginPath();
  for (var i = .1; i <= .9; i += .1) {
    pen.line([i,.1],[i,.9]);
    pen.line([.1,i],[.9,i]);
  }
  ctx.stroke();
  ctx.closePath();
}

pen.drawBoard = drawBoard;
pen.drawGridlines = drawGridlines;

mouse.getId = function(x,y) {
  y = 1 - y;
  if (x < .1) return -1;
  if (y < .1) return -1;
  if (x >= .9) return -1;
  if (y >= .9) return -1;
  x = Math.floor(x*10 - 1);
  y = Math.floor(y*10 - 1);
  mouse.id = y << 3 | x;
}

