function hl() {
  for (var move of state.moves) {
    var id = move.to;
    var m = centers[id];
    if (!m) continue;
    var piece = Piece.at(id);
    ctx.fillStyle = 'black';
    ctx.beginPath();
    ctx.globalAlpha = .25;
    pen.moveTo(m);
    if (piece) {
      pen.arc(m,.05*1,0,2*Math.PI);
      pen.arc(m,.05*.8,0,2*Math.PI,true);
    } else {
      pen.arc(m,.05*.4,0,2*Math.PI);
    }
    ctx.fill();
    ctx.globalAlpha = 1;
    ctx.closePath();
  }
}

